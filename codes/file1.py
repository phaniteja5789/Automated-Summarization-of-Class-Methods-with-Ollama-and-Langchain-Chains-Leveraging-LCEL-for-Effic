class MathUtils:
    def add_numbers(self, a, b):
        """
        Adds two numbers and returns the result.
        """
        return a + b

    def multiply_numbers(self, a, b):
        """
        Multiplies two numbers and returns the result.
        """
        return a * b

    def greet(self, name):
        """
        Prints a greeting message with the given name.
        """
        print(f"Hello, {name}!")

    def is_even(self, number):
        """
        Checks if a number is even and returns True if it is, False otherwise.
        """
        return number % 2 == 0

    def get_square_root(self, number):
        """
        Calculates the square root of a number and returns the result.
        """
        return number ** 0.5